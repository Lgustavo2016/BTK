package Beans;

public class AutorBean {
    
    private int ID;
    private String nome;
    
    public AutorBean() {
    
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }


}
